#include "libft.h"

void	ft_swapint(int *nb, int *nb2)
{
	int nbtmp;

	nbtmp = 0;
	if (nb && nb2)
	{
		nbtmp = *nb;
		*nb = *nb2;
		*nb2 = nbtmp;
	}
}
