#include "libft.h"

void	ft_strappend(char *s1, char *s2, int max)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (s1[i])
		i++;
	while (s2[j] && i < max)
	{
		s1[i] = s2[j];
		j++;
		i++;
	}
	s1[i] = 0;
}
